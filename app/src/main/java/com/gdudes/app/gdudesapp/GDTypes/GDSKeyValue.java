package com.gdudes.app.gdudesapp.GDTypes;

public class GDSKeyValue {
    public String GDSKey;
    public String GDSValue;

    public GDSKeyValue() {
        GDSKey = "";
        GDSValue = "";
    }

    public GDSKeyValue(String vGDSKey, String vGDSValue) {
        GDSKey = vGDSKey;
        GDSValue = vGDSValue;
    }
}
